      String errorMessage = 'Please correct the errors:';
      if (startLocation.isEmpty || endLocation.isEmpty) {
        errorMessage += '\n- Select different start and end locations.';
      }
      if (priceController.text.isEmpty || double.tryParse(priceController.text) == null) {
        errorMessage += '\n- Enter a valid price.';
      }
      ScaffoldMessenger.of(context).showSnackBar(
        SnackBar(content: Text(errorMessage)),
      );
    }
  },
  child: const Text('Continue'),
),
          ],
        ),
      ),
    );
  }
