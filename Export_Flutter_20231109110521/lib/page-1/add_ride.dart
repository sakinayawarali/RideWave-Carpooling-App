    onPrimary: Colors.white,
  ),
  onPressed: () async {
    // Perform validation checks here
    if (startLocation.isNotEmpty &&
        endLocation.isNotEmpty &&
        startLocation != endLocation &&
        priceController.text.isNotEmpty &&
        double.tryParse(priceController.text) != null) {
      // If all validations pass, continue with the Firestore upload
      try {
        // Call your Firestore data upload function here
        await addRideDetails(
          startLocation,
          endLocation,
          selectedTime,
          selectedGender,
          double.parse(priceController.text), // Include the price
        );
        // Navigate to home page after successful upload
