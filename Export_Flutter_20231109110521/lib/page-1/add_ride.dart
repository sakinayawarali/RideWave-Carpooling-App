  Future<void> selectTime(BuildContext context) async {
    final TimeOfDay? pickedTime = await showTimePicker(
      context: context,
      initialTime: TimeOfDay.now(),
    );
    if (pickedTime != null && pickedTime != TimeOfDay.now()) {
      setState(() {
        selectedTime = pickedTime.format(context);
      });
    }
  }

  void selectLocation(BuildContext context, {required bool isStartLocation}) {
    showModalBottomSheet(
      context: context,
      builder: (BuildContext bc) {
        List<String> locationOptions = isStartLocation
            ? locations
            : locations.where((loc) => loc != startLocation).toList();

        if (!isStartLocation &&
            startLocation != 'IBA MAIN CAMPUS' &&
            startLocation != 'IBA CITY CAMPUS') {
          locationOptions = ['IBA MAIN CAMPUS', 'IBA CITY CAMPUS'];
        }

        return SafeArea(
          child: SingleChildScrollView(
            child: Wrap(
              children: locationOptions
                  .map((location) => ListTile(
                        title: Text(location),
                        onTap: () {
                          setState(() {
                            if (isStartLocation) {
                              startLocation = location;
                            } else {
                              endLocation = location;
                            }
                          });
                          Navigator.pop(context);
                        },
                      ))
                  .toList(),
            ),
          ),
        );
      },
    );
  }

